def create_board():
    board.pu()
    board.forward(150)
    def line_down():
        board.pendown()
        board.right(90)
        board.forward(400)
        board.left(180)
        board.forward(800)
        board.forward(-400)
        board.penup()
    line_down()
    board.pu()
    board.left(90)
    board.forward(300)
    line_down()
    board.pu()
    board.left(180)
    board.forward(150)
    def line_left():
        board.pd()
        board.left(90)
        board.forward(800)
        board.forward(-2400)
        board.forward(1600)
        board.right(270)
    line_left()
    board.pu()
    board.forward(300)
    line_left()
    board.forward(1000)

